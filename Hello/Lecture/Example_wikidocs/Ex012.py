"""
012 구분자를 사용한 출력
print() 함수를 사용하여 다음과 같이 출력하라. (힌트: sep 인자 사용)
실행 예:
한놈-두식이-석삼-너구리-오징어
"""
print("한놈","두식이","석삼","너구리","오징어", sep="-")
print("한놈","두식이","석삼","너구리","오징어", sep="~~")
print("한놈","두식이","석삼","너구리","오징어", sep="/")
year = 2000
month = 6
date = 21
print(year,month,date)
print(year,month,date, sep="/")
hour = 14
minute = 45
second = 33
print(hour,minute,second)
print(hour,minute,second, sep=":")
