"""
계산 결과 확인 요망
"""
print(3/2)      # 1.5
print(3//2)     # 1
print(3%2)      # 1 : 몫이 1이고 나머지는 1
print(-3/2)     # -1.5
print(-3//2)    # -2
print(-3%2)     # 1 : 몫이 -1 이고 나머지는 1 (?)
print("="*50)
print(10/3)     # 3.3333333333333335
print(10//3)    # 3
print(10%3)     # 1
print(-10/3)    # -3.3333333333333335
print(-10//3)   # -4
print(-10%3)    # 2 = (4 * 3) - 10
print("="*50)
print(10/4)     # 2.5
print(10//4)    # 2
print(10%4)     # 2
print(-10/4)    # -2.5
print(-10//4)   # -3
print(-10%4)    # 2 = (3 * 4) - 10
print("="*50)
print(10/6)     # 1.6666666666666667
print(10//6)    # 1
print(10%6)     # 4
print(-10/6)    # -1.6666666666666667
print(-10//6)   # -2
print(-10%6)    # 2 = (2 * 6) - 10
print("="*50)
print(10/9)     # 1.1111111111111112
print(10//9)    # 1
print(10%9)     # 1
print(-10/9)    # -1.1111111111111112
print(-10//9)   # -2
print(-10%9)    # 8 = (2 * 9) - 10
print("="*50)
#그렇다면 다음의 값은?
print(-10/20)   # -0.5
print(-10//20)  # -1
print(-10%20)   # 10 = (1 * 20) - 10
#*******************************************************************
# 결론
#   A / B = C
#  A는 피제수(dividend) B는 제수(divisor) 그리고 C는 몫(quotient)
# 양수 % : 피제수  - (//몫 * 제수)
# 음수 % : (//몫 * 제수) - 피제수
