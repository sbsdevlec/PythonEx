# 대입 연산자
i = 8
j = 3
print("{} += {} : ".format(i,j),end=" ")
i += j
print(i)

print("{} -= {} : ".format(i,j),end=" ")
i -= j
print(i)

print("{} *= {} : ".format(i,j),end=" ")
i *= j
print(i)

print("{} /= {} : ".format(i,j),end=" ")
i /= j
print(i)

print("{} //= {} : ".format(i,j),end=" ")
i //= j
print(i)

print("{} %= {} : ".format(i,j),end=" ")
i %= j
print(i)

print("{} **= {} : ".format(i,j),end=" ")
i **= j
print(i)
