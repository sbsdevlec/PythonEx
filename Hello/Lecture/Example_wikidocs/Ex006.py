"""
006 형 변환
문자열 '720'를 정수형으로 변환하라. 정수 100을 문자열 '100'으로 변환하라.
"""
string = '720'
print("type : ", type(string), "값 : ", string)
i = int(string)
print("type : ", type(i), "값 : ", i)

i2 = 100;
print("type : ", type(i2), "값 : ", i2)
string2 = str(i2)
print("type : ", type(string2), "값 : ", string2)
